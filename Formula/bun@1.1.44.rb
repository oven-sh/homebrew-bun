  if OS.mac?
    if Hardware::CPU.arm? || Hardware::CPU.in_rosetta2?
      url "https://github.com/oven-sh/bun/releases/download/bun-v#{version}/bun-darwin-aarch64.zip"
      sha256 "f192297d4570165efaf0ed0ff9939f70c554a639cc56cb7d56963948ea18502e" # bun-darwin-aarch64.zip
    elsif Hardware::CPU.avx2?
      url "https://github.com/oven-sh/bun/releases/download/bun-v#{version}/bun-darwin-x64.zip"
      sha256 "42f64d8ee71ce5c140fa7c4070fd883ecafe6feb7a67c865a06404667b176133" # bun-darwin-x64.zip
    else
      url "https://github.com/oven-sh/bun/releases/download/bun-v#{version}/bun-darwin-x64-baseline.zip"
      sha256 "481404ceb4fe72907282cf1ba8f114195ab9ea6112f7d08204d5418e38eaa584" # bun-darwin-x64-baseline.zip
    end
  elsif OS.linux?
    if Hardware::CPU.arm?
      url "https://github.com/oven-sh/bun/releases/download/bun-v#{version}/bun-linux-aarch64.zip"
      sha256 "cf624eeb474124e80f9512878eac2af740694995bdb6784bef039357eadbba76" # bun-linux-aarch64.zip
